// Step 1: Declare number variables
const num1 = 10;
const num2 = 5;

// Step 2: Arithmetic Operations
const sum = num1 + num2;            // Addition
const difference = num1 - num2;     // Subtraction
const product = num1 * num2;        // Multiplication
const quotient = num1 / num2;       // Division
const remainder = num1 % num2;      // Modulus (Remainder)

// Step 3: Exponentiation
const num3 = num1 ** num2;         // Exponentiation (num1^num2)

// Step 4: Print results to console
console.log(`Sum: ${sum}`);
console.log(`Difference: ${difference}`);
console.log(`Product: ${product}`);
console.log(`Quotient: ${quotient}`);
console.log(`Remainder: ${remainder}`);
console.log(`Exponentiation (num1^num2): ${num3}`);
